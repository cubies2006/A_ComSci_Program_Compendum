public class prob10
{
  public static void main(String[]args)
  {
    System.out.println("The natural log of 10 is " + Math.log(10));
    System.out.println("Absolute value of -10 is " + Math.abs(-10));
    System.out.println("The maximum between 2 & 3 is " + Math.max(2, 3));
    System.out.println("The square root of 7 is " + Math.sqrt(7));
    System.out.println("The 5th power of 6 is " + Math.pow(6, 5));
    System.out.println("The sine of 8 radian is " + Math.sin(8));
    System.out.println("The Random numbers (0.0 to 1.0) is " + Math.random());
  }
}
